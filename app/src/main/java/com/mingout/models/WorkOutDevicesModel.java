package com.mingout.models;

public class WorkOutDevicesModel {

	String gym_device_id, body_part, device_number, device_seat, description,
			time_stamp, status;

	public String getGym_device_id() {
		return gym_device_id;
	}

	public void setGym_device_id(String gym_device_id) {
		this.gym_device_id = gym_device_id;
	}

	public String getBody_part() {
		return body_part;
	}

	public void setBody_part(String body_part) {
		this.body_part = body_part;
	}

	public String getDevice_number() {
		return device_number;
	}

	public void setDevice_number(String device_number) {
		this.device_number = device_number;
	}

	public String getDevice_seat() {
		return device_seat;
	}

	public void setDevice_seat(String device_seat) {
		this.device_seat = device_seat;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public String getTime_stamp() {
		return time_stamp;
	}

	public void setTime_stamp(String time_stamp) {
		this.time_stamp = time_stamp;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

}
